(defn test-num
  [num cc]
  (let [putil (PhoneNumberUtil/getInstance)]
    (try
      (let [parsed (.parse putil num cc)]
        (.isPossibleNumber putil parsed)
        true
        )
      (catch Exception e
        false))))

(defn always-invalid? [num]
  (loop [[code & more] iso-cc]
    (if code
      (if (test-num num code) false (recur more))
      true)))



(defn fake-number [length]
  (apply str (map (constantly "2") (range 0 length))))

